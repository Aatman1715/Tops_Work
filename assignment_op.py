# Basic assignment
x = 10
print(f"x = {x}")
# Add and assign
x += 5
print(f"x += 5: {x}")
# Subtract and assign
x -= 3
print(f"x -= 3: {x}")
# Multiply and assign
x *= 2
print(f"x *= 2: {x}")
# Divide and assign
x /= 4
print(f"x /= 4: {x}")
# Floor divide and assign
x //= 2
print(f"x //= 2: {x}")
# Modulo and assign
x %= 5
print(f"x %= 5: {x}")
# Exponent and assign
x **= 2
print(f"x **= 2: {x}")